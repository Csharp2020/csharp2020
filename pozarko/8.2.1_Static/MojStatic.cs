using System;

namespace _8._2._1_Static
{
    internal class MojStatic
    {
        internal static double Zbroj(double broj1, double broj2)
        {
            return broj1 + broj2;
        }
        internal static double CelzijFahrenheit(double x)
        {
            return x * 9 / 5 + 32;
        }
    }
}